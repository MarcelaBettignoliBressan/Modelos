package orientacao_a_objetos;

public class ModeloClasseAtributosGettersAndSetters {

		//Atributos
		String nome;
		int idade;
		double salario;		
		char sexoFouM;
		boolean ativo;
		
		public String getNome() {
			return nome;
		}
		public void setNome(String nome) {
			this.nome = nome;
		}
		public int getIdade() {
			return idade;
		}
		public void setIdade(int idade) {
			this.idade = idade;
		}
		public double getSalario() {
			return salario;
		}
		public void setSalario(double salario) {
			this.salario = salario;
		}
		public char getSexoFouM() {
			return sexoFouM;
		}
		public void setSexoFouM(char sexoFouM) {
			this.sexoFouM = sexoFouM;
		}
		public boolean isAtivo() {
			return ativo;
		}
		public void setAtivo(boolean ativo) {
			this.ativo = ativo;
		}
		
		
		
		
		
		
}
